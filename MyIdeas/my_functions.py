##zip is a built-in Python function that is used to combine two or more iterables
# (e.g., lists, tuples, or strings) element-wise. It creates an iterator that generates
# tuples containing elements from the input iterables at corresponding positions.

#Here's a simple example of how zip works:

list1 = [1, 2, 3]
list2 = ['a', 'b', 'c']

result = zip(list1, list2)

for item in result:
    print(item)



